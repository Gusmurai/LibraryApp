package ru.library.libraryapp.dao;

import ru.library.libraryapp.domains.Lending;
import java.util.List;

/**
 * Интерфейс уровня доступа к данным (DAO) для управления процессами книговыдачи.
 * Обеспечивает операции регистрации выдачи, возврата, продления и учета утери книг,
 * а также методы получения истории операций для формуляров.
 */
public interface LendingDao {

    /**
     * Оформляет выдачу физического экземпляра книги читателю.
     * Вызывает хранимую процедуру СУБД, выполняющую проверку лимитов и задолженностей.
     *
     * @param ticketNumber    уникальный номер читательского билета
     * @param inventoryNumber инвентарный номер выдаваемого экземпляра
     * @param librarianId     табельный номер библиотекаря, оформившего операцию
     */
    void issueBook(Integer ticketNumber, Integer inventoryNumber, Integer librarianId);

    /**
     * Инициирует процедуру возврата книги в фонд.
     * Проверяет наличие просрочки и возвращает данные о ней для последующего начисления штрафа.
     *
     * @param lendingId   идентификатор активной записи о выдаче
     * @param librarianId табельный номер библиотекаря, принимающего книгу
     */
    void returnBook(Integer lendingId, Integer librarianId);

    /**
     * Окончательно закрывает запись о выдаче (подтверждает возврат).
     * Используется как завершающий этап в цепочке действий после обработки штрафов.
     *
     * @param lendingId идентификатор записи о выдаче
     */
    void closeReturn(Integer lendingId);

    /**
     * Продлевает срок пользования книгой.
     * Вызывает процедуру пересчета плановой даты возврата и инкремента счетчика продлений.
     *
     * @param lendingId идентификатор записи о выдаче
     */
    void renewBook(Integer lendingId);

    /**
     * Регистрирует факт утери книги читателем.
     * Автоматически инициирует процессы начисления штрафа и формирования акта списания.
     *
     * @param lendingId   идентификатор записи о выдаче
     * @param librarianId табельный номер библиотекаря, фиксирующего утерю
     */
    void markAsLost(Integer lendingId, Integer librarianId);

    /**
     * Возвращает список книг, которые в данный момент находятся на руках у читателя.
     *
     * @param ticketNumber номер читательского билета
     * @return список объектов {@link Lending}, где дата возврата не установлена
     */
    List<Lending> findActiveByReader(Integer ticketNumber);

    /**
     * Получает полную историю перемещений конкретного физического экземпляра.
     * Используется для формирования электронного формуляра экземпляра.
     *
     * @param inventoryNumber уникальный инвентарный номер экземпляра
     * @return список всех записей о выдаче данного экземпляра
     */
    List<Lending> findHistoryByInventoryNumber(Integer inventoryNumber);

    /**
     * Получает полную историю всех книг, которые когда-либо брал читатель.
     * Используется для формирования электронного формуляра читателя.
     *
     * @param ticketNumber номер читательского билета
     * @return список всех когда-либо осуществленных выдач данному абоненту
     */
    List<Lending> findHistoryByReader(Integer ticketNumber);
}